# euler39

import time
t0 = time.time()

perimeters = {}

for a in range(1, 9998):
    for b in range(a+1, 999):
        for c in range(b+1, 1000):
            perimeter = a + b + c;
            if perimeter > 1000:
                break
            if a**2 + b**2 == c**2:
                print('a={}, b={}, c={}, sum={}'.format(a,b,c,perimeter))
                if perimeter in perimeters:
                    perimeters[perimeter] += 1
                else:
                    perimeters[perimeter] = 1
                    
most_solutions = max(perimeters, key=lambda key: perimeters[key])
print('\nperimeter sum with the most solutions ({}) = {}'.format(perimeters[most_solutions], most_solutions))

t1 = time.time()
elapsed = t1 - t0
print('\ntime={}'.format(elapsed))



