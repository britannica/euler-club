import time


def get_series_sum(limit):
    n = limit
    series_sum = (n*(2+(n-1)))/2
    return int(series_sum)


def get_factors(n):
    factors = []
    for i in range(1, int(n**0.5) + 1):
        if n % i == 0:
            factors.extend([i, n//i])
    return set(factors)


def get_answer(num):
    found = False
    test_num = 1
    while not found:
        answer = get_series_sum(test_num)
        factors = get_factors(answer)
        if len(factors) > num:
            return "{}: {}".format(answer, factors)
        test_num += 1
    return "not found"


if __name__ == '__main__':
    start = time.time()
    answer = get_answer(500)
    print(answer)
    end = time.time()
    print(end - start)

